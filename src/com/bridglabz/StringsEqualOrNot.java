/*
Start
Initialized two string variables i.e. first and second
if first is not equal to second
Then it will print strings are unequal
else strings are equal
End
 */
public class StringsEqualOrNot {
    public void checkStrings(){
        String first = "Rohan";
        String second = "Rohan";
        if (first != second){
            System.out.println("Given strings are unequal.");
        }
        else{
            System.out.println("Strings are equals.");
        }
    }
}
